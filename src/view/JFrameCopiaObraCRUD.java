    public JFrameCopiaObraCRUD(CopiaObra copiaObra, boolean disconnectOnClose) throws Exception {
        initComponents();
        
        setTitle("Cadastro de Cópia de Obra Literária");
        
        if(copiaObra == null) {
            this.copiaObra = new CopiaObra();
        } else {
            this.copiaObra = copiaObra;
            
            if(this.copiaObra.getObraLiteraria() != null){
                this.obraLiteraria = new ObraLiteraria();
                this.obraLiteraria.setCodObraLiteraria(this.copiaObra.getObraLiteraria().getCodObraLiteraria());
                this.obraLiteraria.load();
            }
            
            dataUp();
            jTextFieldID.setEnabled(false);
        }
        
        this.disconnectOnClose = disconnectOnClose;
    }
    
    private void checkInput() throws Exception {
        
        if(jTextFieldID.getText().isEmpty()) {
            throw new Exception("Informe ID.");
        } else {
            if(!jTextFieldID.getText().matches("\\d+")) {
                throw new Exception("O campo ID deve ser um número.");
            }
        }
        
        if(jTextFieldObraLiteraria.getText().isEmpty()) {
            throw new Exception("Selecione uma Obra Literária.");
        }        
    }
    
    private void dataDown() throws Exception {
        copiaObra.setCodCopiaObra(Integer.parseInt(jTextFieldID.getText()));
        
        if(jCheckBoxSituacao.isSelected()) {
            copiaObra.setSituacao("S");
        } else {
            copiaObra.setSituacao("N");
        }
        
        copiaObra.setObraLiteraria(obraLiteraria);
    }
    
    private void dataUp() {
        jTextFieldID.setText(String.valueOf(copiaObra.getCodCopiaObra()));

        if((copiaObra.getSituacao() != null) && (copiaObra.getSituacao().equals("S"))){
            jCheckBoxSituacao.setSelected(true);
        } else {
            jCheckBoxSituacao.setSelected(false);
        }
        
        if(copiaObra.getObraLiteraria().getTitulo() != null) {
            jTextFieldObraLiteraria.setText(copiaObra.getObraLiteraria().getTitulo());
        }
    }
    
