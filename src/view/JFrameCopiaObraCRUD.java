    private void jButtonSelecionarObraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelecionarObraActionPerformed
        try {
            if(obraLiteraria == null) {
                obraLiteraria = new ObraLiteraria();
            }
            
            JFrameConsultaObraLiteraria jFrameConsulta;
            jFrameConsulta = new JFrameConsultaObraLiteraria(true, false, obraLiteraria);
            
            jFrameConsulta.addWindowListener(new java.awt.event.WindowAdapter(){
                @Override
                public void windowClosed(java.awt.event.WindowEvent evt) {
                    if(obraLiteraria.getTitulo() != null) {
                        jTextFieldObraLiteraria.setText(obraLiteraria.getTitulo());
                    }
                }
            } );
            
            jFrameConsulta.setVisible(true);
        } catch(Exception ex) {
            ex.printStackTrace();
            obraLiteraria = null;
        }
    }//GEN-LAST:event_jButtonSelecionarObraActionPerformed

    private void jButtonExcluirObraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirObraActionPerformed
        obraLiteraria = null;
        jTextFieldObraLiteraria.setText(null);
    }//GEN-LAST:event_jButtonExcluirObraActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        System.out.println("Excluir");

        try {
            checkInput();
            dataDown();
            copiaObra.delete();
            this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
        } catch(Exception ex) {
            LogTrack.getInstance().addException(ex, true, this);
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        System.out.println("Salvar");

        try {
            checkInput();
            dataDown();
            copiaObra.save();
            this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
        } catch(Exception ex) {            
            LogTrack.getInstance().addException(ex, true, this);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

